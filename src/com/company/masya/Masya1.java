package com.company.masya;


public class Masya1 {

//Необходимо вывести на экран вот такую последовательность чисел:
//100 90 80 70 60 50 40 30 20 10

    public static void main(String[] args) {

        int i = 100;


        while (i >= 10) {

            System.out.print(i + " ");

            i = i - 10;
        }
    }
}



